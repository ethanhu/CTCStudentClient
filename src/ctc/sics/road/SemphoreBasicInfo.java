package ctc.sics.road;

public class SemphoreBasicInfo {

	public String semphoreName; //信号机名称
	public String semphoreType; //信号机类型
	
	public String getSemphoreName() {
		return semphoreName;
	}
	public void setSemphoreName(String semphoreName) {
		this.semphoreName = semphoreName;
	}
	public String getSemphoreType() {
		return semphoreType;
	}
	public void setSemphoreType(String semphoreType) {
		this.semphoreType = semphoreType;
	}
	
	
	
}
